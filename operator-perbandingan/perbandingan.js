let number = 5;

document.writeln('<p>', number > 10 , '</p>'); //false

document.writeln('<p>', number < 10 , '</p>'); //true

document.writeln('<p>', number >= 5 , '</p>'); //true

document.writeln('<p>', number <= 5 , '</p>'); //true

document.writeln('<p>', number == 5 , '</p>'); //true

document.writeln('<p>', number == '5' , '</p>'); //true

document.writeln('<p>', number === '10' , '</p>'); //false

document.writeln('<p>', number != 5 , '</p>'); //false

document.writeln('<p>', number !== 5 , '</p>'); //false


